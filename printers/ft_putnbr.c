#include "../libft.h"

void	ft_putnbr(int n)
{
	long	i;

	i = 0;
	if (n == -2147483648)
		return (ft_putstr(("-2147483648")));
	if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
	}
	if (n > 9)
		ft_putnbr((n / 10));
	ft_putchar(((n % 10) + '0'));
}
